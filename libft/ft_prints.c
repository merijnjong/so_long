#include "libft.h"

int	ft_prints(char *s)
{
	int	chars;

	chars = 0;
	if (!s)
	{
		return (write(1, "(null)", 6));
	}
	while (*s != '\0')
	{
		chars += ft_printc(*s);
		s++;
	}
	return (chars);
}
